#TIPOS PRIMITIVOS
a = input('digite algo: ')
print('o tipo de primitivo de {} é ' .format(a), type(a))
print('{} tem espaços? ' .format(a), a.isspace())
print('{} é numerico? ' .format(a), a.isnumeric())
print('{} é alfabetico? ' .format(a), a.isalpha())
print('{} é alfanumerico? ' .format(a), a.isalnum())
print('{} está em maiúcula? ' .format(a), a.isupper())
print('{} esta em minuscula? ' .format(a), a.islower())
print('{} esta capitalizada? ' .format(a), a.istitle())








 




